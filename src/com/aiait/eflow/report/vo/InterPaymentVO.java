package com.aiait.eflow.report.vo;

import java.math.BigDecimal;
import java.sql.Date;

import com.aiait.framework.vo.BaseVO;

public class InterPaymentVO extends BaseVO {

    protected Date date;
    protected String noteNo;
    protected String requestNo;
    protected String costOrg;
    protected String requestOrg;
    protected String team;
    protected String requestBy;
    protected String content;
    protected BigDecimal amount;
    protected String invStatus;
    protected String status;

    /**
     * @return the date
     */
    public Date getDate() {
        return date;
    }

    /**
     * @param date
     *            the date to set
     */
    public void setDate(Date date) {
        this.date = date;
    }

    /**
     * @return the noteNo
     */
    public String getNoteNo() {
        return noteNo;
    }

    /**
     * @param noteNo
     *            the noteNo to set
     */
    public void setNoteNo(String noteNo) {
        this.noteNo = noteNo;
    }

    /**
     * @return the requestNo
     */
    public String getRequestNo() {
        return requestNo;
    }

    /**
     * @param requestNo
     *            the requestNo to set
     */
    public void setRequestNo(String requestNo) {
        this.requestNo = requestNo;
    }

    /**
     * @return the costOrg
     */
    public String getCostOrg() {
        return costOrg;
    }

    /**
     * @param costOrg
     *            the costOrg to set
     */
    public void setCostOrg(String costOrg) {
        this.costOrg = costOrg;
    }

    /**
     * @return the requestOrg
     */
    public String getRequestOrg() {
        return requestOrg;
    }

    /**
     * @param requestOrg
     *            the requestOrg to set
     */
    public void setRequestOrg(String requestOrg) {
        this.requestOrg = requestOrg;
    }

    /**
     * @return the team
     */
    public String getTeam() {
        return team;
    }

    /**
     * @param team
     *            the team to set
     */
    public void setTeam(String team) {
        this.team = team;
    }

    /**
     * @return the requestBy
     */
    public String getRequestBy() {
        return requestBy;
    }

    /**
     * @param requestBy
     *            the requestBy to set
     */
    public void setRequestBy(String requestBy) {
        this.requestBy = requestBy;
    }

    /**
     * @return the content
     */
    public String getContent() {
        return content;
    }

    /**
     * @param content
     *            the content to set
     */
    public void setContent(String content) {
        this.content = content;
    }

    /**
     * @return the amount
     */
    public BigDecimal getAmount() {
        return amount;
    }

    /**
     * @param amount
     *            the amount to set
     */
    public void setAmount(BigDecimal amount) {
        this.amount = amount;
    }

    /**
     * @return the invStatus
     */
    public String getInvStatus() {
        return invStatus;
    }

    public String getInvStatus2() {
        return invStatus == null ? "Needless" : invStatus.equals("01") ? "Yes" : "No";
    }

    /**
     * @param invStatus
     *            the invStatus to set
     */
    public void setInvStatus(String invStatus) {
        this.invStatus = invStatus;
    }

    /**
     * @return the status
     */
    public String getStatus() {
        return status;
    }

    /**
     * @param status
     *            the status to set
     */
    public void setStatus(String status) {
        this.status = status;
    }

}
