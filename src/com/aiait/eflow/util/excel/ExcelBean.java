package com.aiait.eflow.util.excel;

public class ExcelBean {

	private String sheetName;
	private int sheetIndex = 0;
	
	private int startRow;
	private int endRow;
	
	private int startCell;
	private int endCell;
	
	private boolean isErrorData;
	/**
	 * @return the isErrorData
	 */
	public boolean isErrorData() {
		return isErrorData;
	}
	/**
	 * @param isErrorData the isErrorData to set
	 */
	public void setErrorData(boolean isErrorData) {
		this.isErrorData = isErrorData;
	}
	/**
	 * @return the sheetName
	 */
	public String getSheetName() {
		return sheetName;
	}
	/**
	 * @param sheetName the sheetName to set
	 */
	public void setSheetName(String sheetName) {
		this.sheetName = sheetName;
	}
	/**
	 * @return the sheetIndex
	 */
	public int getSheetIndex() {
		return sheetIndex;
	}
	/**
	 * @param sheetIndex the sheetIndex to set
	 */
	public void setSheetIndex(int sheetIndex) {
		this.sheetIndex = sheetIndex;
	}
	/**
	 * @return the startRow
	 */
	public int getStartRow() {
		return startRow;
	}
	/**
	 * @param startRow the startRow to set
	 */
	public void setStartRow(int startRow) {
		this.startRow = startRow;
	}
	/**
	 * @return the endRow
	 */
	public int getEndRow() {
		return endRow;
	}
	/**
	 * @param endRow the endRow to set
	 */
	public void setEndRow(int endRow) {
		this.endRow = endRow;
	}
	/**
	 * @return the startCell
	 */
	public int getStartCell() {
		return startCell;
	}
	/**
	 * @param startCell the startCell to set
	 */
	public void setStartCell(int startCell) {
		this.startCell = startCell;
	}
	/**
	 * @return the endCell
	 */
	public int getEndCell() {
		return endCell;
	}
	/**
	 * @param endCell the endCell to set
	 */
	public void setEndCell(int endCell) {
		this.endCell = endCell;
	}
	/**
	 * constructor
	 * @param sheetIndex sheetIndex
	 * @param startRow startRow
	 * @param endRow endRow
	 * @param startCell startCell
	 * @param endCell endCell
	 */
	public ExcelBean(int sheetIndex, int startRow, int endRow, int startCell,
			int endCell) {
		super();
		this.sheetIndex = sheetIndex;
		this.startRow = startRow;
		this.endRow = endRow;
		this.startCell = startCell;
		this.endCell = endCell;
	}
	public ExcelBean(int endRow, int endCell) {
		super();
		this.endRow = endRow;
		this.endCell = endCell;
	}
	
}
