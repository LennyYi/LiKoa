package com.aiait.eflow.housekeeping.vo;

import com.aiait.framework.vo.BaseVO;

public class OperateLogVO extends BaseVO {
	private int id;

	private String operateStaffCode;

	private String operateDateStr;

	private String operateType; // 01----unlock form;  02-----lock form; 03-----reassign form

	private String operateDescription;

	private String remark;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getOperateDateStr() {
		return operateDateStr;
	}

	public void setOperateDateStr(String operateDateStr) {
		this.operateDateStr = operateDateStr;
	}

	public String getOperateDescription() {
		return operateDescription;
	}

	public void setOperateDescription(String operateDescription) {
		this.operateDescription = operateDescription;
	}

	public String getOperateStaffCode() {
		return operateStaffCode;
	}

	public void setOperateStaffCode(String operateStaffCode) {
		this.operateStaffCode = operateStaffCode;
	}

	public String getOperateType() {
		return operateType;
	}

	public void setOperateType(String operateType) {
		this.operateType = operateType;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

}
